
package Entidad;


public class Clase_Rectangulo {
    
    public int base;
    public int altura;

    public Clase_Rectangulo() {
    }

    public Clase_Rectangulo(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
    
}
